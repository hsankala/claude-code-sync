#!/bin/bash
# generate-claude-memory.sh
# Rolls up all markdown files in ai-docs/ into CLAUDE.md in the project root.
# Run this whenever you update or add docs in ai-docs/.

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(dirname "$SCRIPT_DIR")"
AI_DOCS_DIR="$PROJECT_ROOT/ai-docs"
OUTPUT_FILE="$PROJECT_ROOT/CLAUDE.md"

echo "┌──────────────────────────────────────┐"
echo "│     Generating CLAUDE.md memory      │"
echo "└──────────────────────────────────────┘"
echo ""

# Write header
cat > "$OUTPUT_FILE" << 'EOF'
# CLAUDE.md — AI Context for claude-ops-oci-arm-main

This file is auto-generated by tools/generate-claude-memory.sh
Do not edit directly — edit the source files in ai-docs/ instead.

---

EOF

# Define document order — add new docs here in the order you want them
doc_order=(
    "about-server.md"
    "server-networking.md"
    "sudo-context.md"
    "speech-to-text.md"
    "command-briefing.md"
    "claude-git-usage.md"
    "chat-output-formatting.md"
    "good-command-patterns.md"
    "message-formatting.md"
)

# Track counts
found=0
missing=0

# Append documents in defined order
for filename in "${doc_order[@]}"; do
    doc="$AI_DOCS_DIR/$filename"
    if [ -f "$doc" ]; then
        echo "[✓] Including: $filename"
        echo "## $filename" >> "$OUTPUT_FILE"
        echo "" >> "$OUTPUT_FILE"
        cat "$doc" >> "$OUTPUT_FILE"
        echo "" >> "$OUTPUT_FILE"
        echo "---" >> "$OUTPUT_FILE"
        echo "" >> "$OUTPUT_FILE"
        ((found++))
    else
        echo "[!] Missing:   $filename"
        ((missing++))
    fi
done

# Write summary footer
cat >> "$OUTPUT_FILE" << EOF

## Generation Summary

- **Files included:** $found
- **Generated:** $(date '+%Y-%m-%d %H:%M:%S')
- **Source:** $AI_DOCS_DIR
- **Output:** $OUTPUT_FILE
EOF

echo ""
echo "[✓] Done — $found file(s) rolled up into CLAUDE.md"
echo "    Output: $OUTPUT_FILE"
